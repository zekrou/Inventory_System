var currentCategoryId = null;

function manageBrandsAttributes(categoryId) {
  currentCategoryId = categoryId;
  
  $.ajax({
    url: 'fetchCategoryDataById/' + categoryId,
    type: 'post',
    dataType: 'json',
    success: function(category) {
      $('#category_name_display').text('Category: ' + category.name);
      
      // Populate brands
      var brandsHtml = '';
      <?php foreach($all_brands as $brand): ?>
        var checked = category.brand_ids.includes(<?php echo $brand['id']; ?>) ? 'checked' : '';
        brandsHtml += '<div class="checkbox"><label><input type="checkbox" name="brand_ids[]" value="<?php echo $brand['id']; ?>" ' + checked + '> <?php echo $brand['name']; ?></label></div>';
      <?php endforeach; ?>
      $('#brands_list').html(brandsHtml);
      
      // Populate attributes
      var attrsHtml = '';
      <?php foreach($all_attributes as $attr): ?>
        var checked = category.attribute_ids.includes(<?php echo $attr['id']; ?>) ? 'checked' : '';
        attrsHtml += '<div class="checkbox"><label><input type="checkbox" name="attribute_ids[]" value="<?php echo $attr['id']; ?>" ' + checked + '> <?php echo $attr['name']; ?></label></div>';
      <?php endforeach; ?>
      $('#attributes_list').html(attrsHtml);
      
      $('#manageBrandsAttributesModal').modal('show');
    }
  });
}

function saveBrandsAttributes() {
  var brandIds = [];
  $('input[name="brand_ids[]"]:checked').each(function() {
    brandIds.push($(this).val());
  });
  
  var attributeIds = [];
  $('input[name="attribute_ids[]"]:checked').each(function() {
    attributeIds.push($(this).val());
  });
  
  // Save brands
  $.post('category/assignBrands/' + currentCategoryId, {brand_ids: brandIds}, function(response) {
    // Save attributes
    $.post('category/assignAttributes/' + currentCategoryId, {attribute_ids: attributeIds}, function(response2) {
      alert('Saved successfully!');
      $('#manageBrandsAttributesModal').modal('hide');
      manageTable.ajax.reload();
    }, 'json');
  }, 'json');
}
